package java;

public class Day5_1 {
public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		//Relational Operators (Comparison operators)--->   == < >  <=  >=  !=
		//Always return boolean value
		System.out.println("------------------------Relational operators---------------------");		
		System.out.println(a==b); //false
		System.out.println(a>b); //false
		System.out.println(a<b); //true
		System.out.println(a>=b);//false
		System.out.println(a<=b); //true
		System.out.println(a!=b); //true
				

}
}
